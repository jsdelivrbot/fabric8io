package io.fabric8.collector.elasticsearch;

import com.fasterxml.jackson.annotation.JsonProperty;

/**
 * Represents the results from inserting data
 */
public class ResultsDTO extends DTOSupport {
    @JsonProperty("_index")
    private String index;
    @JsonProperty("_type")
    private String type;
    @JsonProperty("_id")
    private String id;
    @JsonProperty("_version")
    private String version;
    private Boolean created;
    @JsonProperty("_shards")
    private ShardsDTO shards;

    public Boolean getCreated() {
        return created;
    }

    public void setCreated(Boolean created) {
        this.created = created;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getIndex() {
        return index;
    }

    public void setIndex(String index) {
        this.index = index;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public ShardsDTO getShards() {
        return shards;
    }

    public void setShards(ShardsDTO shards) {
        this.shards = shards;
    }
}
